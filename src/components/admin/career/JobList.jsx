export const getStatusBadge = (status) => {
  const statusMap = {
    'Open': 'bg-green-100 text-green-800',
    'Closed': 'bg-red-100 text-red-800',
    'Draft': 'bg-yellow-100 text-yellow-800'
  };
  return statusMap[status] || 'bg-gray-100 text-gray-800';
};

